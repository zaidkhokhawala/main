#include <iostream>
using namespace std;

 main() {
    // Variables
    int a = 10, b = 20;
    float x = 5.5, y = 2.3;
    
    // Constant
    const float PI = 3.14;

    // Operations
    int sum = a + b;
    float product = x * y;
    float circleArea = PI * x * x;

    // Output results
    cout << "Sum of a and b: " << sum << endl;
    cout << "Product of x and y: " << product << endl;
    cout << "Area of circle: " << circleArea << endl;

}

